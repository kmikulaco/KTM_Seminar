//Funkcija za postavljanje Wakeup Timer-a (interval u minutama)
void set_wakeup_timer_minutes(uint32_t minutes) {
	// Pretvori minute u sekunde (max 65535 sekundi)
	uint32_t seconds = minutes * 60;
	if (seconds > 65535)
		seconds = 65535;

	HAL_RTCEx_DeactivateWakeUpTimer(&hrtc);

	if (HAL_RTCEx_SetWakeUpTimer_IT(&hrtc, seconds,
	RTC_WAKEUPCLOCK_CK_SPRE_16BITS) != HAL_OK) {
		Error_Handler();
	}
}

//Funkcija za postavljanje Wakeup Timer-a (interval u minutama)
void set_wakeup_timer_seconds(uint32_t seconds) {

	HAL_RTCEx_DeactivateWakeUpTimer(&hrtc);

	if (HAL_RTCEx_SetWakeUpTimer_IT(&hrtc, seconds,
	RTC_WAKEUPCLOCK_CK_SPRE_16BITS) != HAL_OK) {
		Error_Handler();
	}
}

// 3. Zaustavi Wakeup Timer
void stop_wakeup_timer(void) {
	HAL_RTCEx_DeactivateWakeUpTimer(&hrtc);
}

// Dohvati trenutno vrijeme
uint8_t get_current_hour(void) {
	RTC_TimeTypeDef sTime = { 0 };
	RTC_DateTypeDef sDate = { 0 };

	HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
	HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);

	return sTime.Hours;
}

// Je li dan? (10:00 - 22:00)
uint8_t is_day_time(void) {
	uint8_t hour = get_current_hour();
	return (hour >= 10 && hour < 22);
}

uint8_t get_current_minute(void) {
	RTC_TimeTypeDef sTime = { 0 };
	RTC_DateTypeDef sDate = { 0 };

	HAL_RTC_GetDate(&hrtc, &sDate, RTC_FORMAT_BIN);
	HAL_RTC_GetTime(&hrtc, &sTime, RTC_FORMAT_BIN);

	return sTime.Minutes;
}

